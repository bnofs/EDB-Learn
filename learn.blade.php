                <br/><br/>5. tubers boiled in water by a slow fire, and then eaten. :"That country [lands around Lake Michigan] also produces potatoes, some are as large as an egg, others have the size of one's fist, or a little more. They boil these in water by a slow fire during twenty-four hours, when they are thoroughly cooked, you will find in them an excellent flavor, much resembling that of prunes" (Perrot 1680-1718, translation from French to English by Blair 1911:117, Perrot's description may refer to Psoralea esculenta, Helianthus tuberosa or another species producing sizeable tubers, this ref. also published by Dunn ca. 1900);
